                Perubahan Pembelajaran Matematika yang Ideal
            </h4>
            <p>Seorang guru sudah seharusnya tidak hanya mengajari dengan cara mengumumkan (telling) atau hanya untuk pemahaman (understanding) saja, namun para guru dituntut juga untuk memfasilitasi siswanya untuk berfikir sehingga mereka dapat menjadi siswa yang mampu belajar secara mandiri (independent learners), berpikir maju, dan kreatif. Sehingga para siswa kita diharapkan akan menjadi Warga Negara Indonesia yang secara bersama-sama ikut bertanggung jawab dan peduli terhadap nasib bangsa, negara , dan warganya. Sejalan dengan itu, pada rasionalitas penambahan jam pelajaran (Kemdiknbud, 2012:11 ) dinyatakan perubahan proses pembelajaran (dari siswa diberi tahu menjadi siswa mencari tahu). Secara umum dapat disampaikan bahwa perubahan pembelajaran yang diinginkan adalah perubahan pembelajaran dari: (1) mengingat (memorizing) atau menghafal (rote learning) kearah berfikir (thingking) dan pemahaman (understanding) dan (2) model ceramah ke pendekatan: discovery learning, problem based approach, inductive learning, atau inquiry learning.</p>
            <img src="blog3.png" alt="">
            <p>Kurikulum 2013 menyatakan dalam artikel Fadjar Shadiq (2014) bahwa: “Berpengetahuan (melalui core subjects) saja tidak cukup, harus dilengkapi: (1) kemampuan kreatif-kritis dan (2) karakter kuat (bertanggung jawab, social, toleran, produktif, adaptif,… )”. Di samping itu juga dinyatakan bahwa harus memiliki ketrampilan. Ketrampilan tersebit dimaksutkan dapat menghasilkan pribadi yang berkemampuan pikir dan tindak yang efektif dan kreatif dalam ranah abstrak dan konkret. Untuk itu para siswa diharapkan dapat belajar untuk mengamati, menanya, mencoba, mengolah, menyaji, menalar, membentuk jejaring dan mencipta atau disebut juga dengan model pembelajaran scientific approach. </p>
            <p>Pembelajaran dengan metode penemuan (scientific approach) membutuhkan waktu yang lebih lama jika dibandingkan dengan metode ekspositori karena kegiatan ini mengembangkan konsep maupun keterampilan matematika dalam kaitannya dengan pemecahan masalah. Untuk membuat prosedur ini menjadi lebih efisien, guru harus mengkonstruksikan masalah itu secara hati-hati atau mengajukan pertanyaan-pertanyaan kunci. </p>
            <p>Apakah belajar dengan menemukan itu penting?</p>
            <p>
                Belajar melalui penemuan itu penting, sebab :<br>
1. Pada kenyataan ilmu-ilmu itu diperoleh melalui penemuan;<br>
2. Matematika adalah bahasa yang abstrak, konsep dan lain-lainnya itu akan lebih melekat bila melalui penemuan dengan jalan memanipulasi dan berpengalaman dengan benda benda kongkrit<br>
3. Generalisasi itu penting, melalui penemuan generalisasi yang diperoleh akan lebih mantap<br>
4. Dapat meningkatkan kemampuan memecahkan masalah<br>
5. Setiap anak adalah makhluk kreatif<br>
6. Menemukan sesuatu oleh sendiri dapat menumbuhkan rasa percaya terhadap dirinya sendiri, dapat meningkatkan motivasi (termasuk motivasi intrinsik), melakukan pengkajian lebih lanjut; pada umumnya bersikap positif terhadap matematika.
            </p>
            <p>Untuk itu proses pembelajaran matematika diantaranya ialah:</p>
            <p>
                a.	Pembelajaran matematika harus menyenangkan bagi siswa dimana guru harus menghindari pembelajaran yang monoton dan rutin dan dalam suasana pembelajaran yang tidak mencemaskan siswa<br>
b.	Harus bermakna dimana pengetahuan baru yang akan dipelajari siswa barus berkait dengan pengetahuan lama yang sudah dimiliki dan dipelajari siswa<br>
c.	Pembelajaran matematika harus membantu siswa belajar berpikir dimana guru harus memfasilitasi siswa agar pembelajarannya focus pada proses pemecahan masalah<br>
d.	Pembelajaran siswa harus membantu siswa untuk menjadi peserta didik yang mandiri di mana guru memfasilitasi siswa agar memulai pembelajaran dengan masalah dan membantu siswa untuk bereksplorasi atau melakukan penyelidikan sendiri.<br>
            </p>
